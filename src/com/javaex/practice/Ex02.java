package com.javaex.practice;

public class Ex02 {

	public static void main(String[] args) {
	/*  02. double 형 배열 을 작성하고 
		1.2, 3.3, 6.7 값을 순서대로 입력하고 아래와 같이 출력되도록 
		for문을 작성 */
		
		double[] num = new double[3];
		
		num[0] = 1.2;
		num[1] = 3.3;
		num[2] = 6.7;
		
		for(int i = 2; i<=2; i--) {
			System.out.println(num[i]);
		}
		

	}

}
